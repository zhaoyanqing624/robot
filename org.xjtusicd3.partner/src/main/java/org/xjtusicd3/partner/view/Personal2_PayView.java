package org.xjtusicd3.partner.view;

public class Personal2_PayView {
	private String userId;
	private String userName;
	private String userImage;
	private String payNumber;
	private String bepayNumber;
	private String work;
	private String isTogetherPay;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserImage() {
		return userImage;
	}
	public void setUserImage(String userImage) {
		this.userImage = userImage;
	}
	public String getPayNumber() {
		return payNumber;
	}
	public void setPayNumber(String payNumber) {
		this.payNumber = payNumber;
	}
	public String getBepayNumber() {
		return bepayNumber;
	}
	public void setBepayNumber(String bepayNumber) {
		this.bepayNumber = bepayNumber;
	}
	public String getWork() {
		return work;
	}
	public void setWork(String work) {
		this.work = work;
	}
	public String getIsTogetherPay() {
		return isTogetherPay;
	}
	public void setIsTogetherPay(String isTogetherPay) {
		this.isTogetherPay = isTogetherPay;
	}
}
